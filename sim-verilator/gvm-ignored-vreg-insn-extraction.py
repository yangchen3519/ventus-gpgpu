WARNING_PATTERNS = (
    "GVM warning in `gvm_t::getDutVRegWbFinish`: ignoring VReg Writeback from",
    "GVM WARN[VREG_IGNORED]: ignoring VReg writeback from",
)

INSN_RE = re.compile(r"insn\s+0x([0-9a-fA-F]{8})")


def extract_ignored_vector_insns(log_text):
    insns = set()
    for line in log_text.splitlines():
        if not any(pattern in line for pattern in WARNING_PATTERNS):
            continue
        match = INSN_RE.search(line)
        if match:
            insns.add(int(match.group(1), 16))
    return sorted(insns)


def format_whitelist_entries(insns):
    matched = []
    for insn in insns:
        for mask, value, name in VECTOR_INSN_PATTERNS:
            if (insn & mask) == value:
                matched.append((mask, value, name))
    unique_entries = sorted(set(matched), key=lambda item: (item[2], item[0], item[1]))
    return [
        f'  {{0x{mask:08x}, 0x{value:08x}, "{name:<22}"}},\\'
        for mask, value, name in unique_entries
    ]


def main():
    parser = argparse.ArgumentParser(
        description="Extract copy-ready vector whitelist entries from GVM logs."
    )
    parser.add_argument("--input", required=True, help="Input GVM log path")
    parser.add_argument("--output", required=True, help="Output whitelist text path")
    args = parser.parse_args()

    try:
        with open(args.input, "r", encoding="utf-8", errors="replace") as file_obj:
            log_text = file_obj.read()
    except OSError as exc:
        print(f"Failed to read input log: {exc}", file=sys.stderr)
        sys.exit(1)

    output_lines = format_whitelist_entries(extract_ignored_vector_insns(log_text))

    try:
        with open(args.output, "w", encoding="utf-8") as file_obj:
            if output_lines:
                file_obj.write("\n".join(output_lines) + "\n")
            else:
                file_obj.write("No ignored vector instructions found.\n")
    except OSError as exc:
        print(f"Failed to write output file: {exc}", file=sys.stderr)
        sys.exit(1)

    print(f"Generated whitelist candidates: {args.output}")


if __name__ == "__main__":
    main()
