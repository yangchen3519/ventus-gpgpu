def extract_unique_xreg_errors(lines):
    """
    提取并去重标量寄存器错误
    返回 OrderedDict: key -> {'count': int, 'line': str}
    """
    results = OrderedDict()
    for raw in lines:
        line = raw.rstrip("\n")
        if ERROR_MARK in line:
            m = RE_DUT_REF.search(line)
            if m:
                key = (m.group(1).lower(), m.group(2).lower())
            else:
                key = ("_LINE_", line)
            if key in results:
                results[key]['count'] += 1
            else:
                results[key] = {'count': 1, 'line': line}
    return results

def extract_vreg_whitelist_code(lines, db):
    """
    提取被忽略的向量指令，并生成 C++ 白名单代码
    返回 list of strings (代码行)
    """
    # 1. 收集所有被忽略的指令机器码
    ignored_insns = set()
    for line in lines:
        m = RE_VREG_IGNORE.search(line)
        if m:
            insn_hex = m.group(1)
            ignored_insns.add(int(insn_hex, 16))
    
    if not ignored_insns:
        return []

    # 2. 匹配数据库生成代码
    matched_code = []
    for insn in ignored_insns:
        found = False
        for entry in db:
            if (insn & entry['mask']) == entry['value']:
                # 格式化为 C++ 代码: {mask, value, "NAME"},
                # 注意：为了对齐美观，名字后面补空格
                name_padded = f"{entry['name']:<20}"
                code_line = f'{{0x{entry["mask"]:08x}, 0x{entry["value"]:08x}, "{name_padded}"}},\\'
                matched_code.append(code_line)
                found = True
                break # 找到一个匹配即可
        if not found:
            # 如果没找到匹配，也输出一条注释
            matched_code.append(f"// Unknown instruction: 0x{insn:08x}")
            
    return matched_code

def main():
    parser = argparse.ArgumentParser(description="GVM 日志处理脚本：合并标量去重与向量白名单生成")
    parser.add_argument("--input", required=True, help="输入 GVM 日志文件路径")
    parser.add_argument("--output", required=True, help="输出结果文件路径")
    args = parser.parse_args()

    # 1. 读取日志文件
    try:
        with open(args.input, "r", encoding="utf-8", errors="replace") as f:
            lines = f.readlines()
    except Exception as e:
        print(f"Error reading input file: {e}", file=sys.stderr)
        sys.exit(1)

    # 2. 准备数据
    insn_db = get_instruction_db()
    
    # 3. 执行处理
    # A. 向量指令白名单
    whitelist_code = extract_vreg_whitelist_code(lines, insn_db)
    
    # B. 标量报错去重
    unique_errors = extract_unique_xreg_errors(lines)
    
    # C. 过滤后的日志 (不包含标量错误行)
    filtered_log = [line for line in lines if ERROR_MARK not in line]

    # 4. 写入输出文件
    try:
        with open(args.output, "w", encoding="utf-8") as f:
            # Section 1: 向量指令白名单
            f.write("=======================================================\n")
            f.write("SECTION 1: Vector Instruction Whitelist Candidates\n")
            f.write("=======================================================\n")
            if whitelist_code:
                for code in whitelist_code:
                    f.write(code + "\n")
            else:
                f.write("No ignored vector instructions found.\n")
            f.write("\n\n")

            # Section 2: 整合的标量寄存器报错
            f.write("=======================================================\n")
            f.write("SECTION 2: Unique Scalar Register (xreg) Errors\n")
            f.write("=======================================================\n")
            if unique_errors:
                for key, info in unique_errors.items():
                    count = info['count']
                    line = info['line']
                    f.write(f"[{count}] {line}\n")
            else:
                f.write("No scalar register errors found.\n")
            f.write("\n\n")

            # Section 3: 过滤后的日志
            f.write("=======================================================\n")
            f.write("SECTION 3: Filtered Log (Scalar Errors Removed)\n")
            f.write("=======================================================\n")
            for line in filtered_log:
                f.write(line)
                
        print(f"Successfully processed log. Output written to: {args.output}")

    except Exception as e:
        print(f"Error writing output file: {e}", file=sys.stderr)
        sys.exit(1)

if __name__ == "__main__":
    main()